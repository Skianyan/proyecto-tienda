Route::get('/', function () {
    return view('welcome');
});

Route::get('/addtocart/{id}', [ProductController::class, 'getAddToCart'])->name('product.addToCart');

Route::get('/shoppingcart', [ProductController::class, 'getCart'])->name('product.shoppingcart');

Route::get('/product', [ProductController::class, 'index'])->name('product.index');

Route::get('/reduce/{id}', [ProductController::class, 'reduceByOne'])->name('product.reduceByOne');
Route::get('/remove/{id}', [ProductController::class, 'removeItem'])->name('product.removeItem');

Route::middleware('auth')->group(function () {

Route::get('/product/create', [ProductController::class, 'create'])->name('product.create');
Route::post('/product/store', [ProductController::class, 'store'])->name('product.store');
Route::get('/product/{product}/edit', [ProductController::class, 'edit'])->name('product.edit');
Route::put('/product/{product}/update', [ProductController::class, 'update'])->name('product.update');
Route::delete('/product/{product}/destroy', [ProductController::class, 'destroy'])->name('product.destroy');
});

Route::get('/dashboard', function () {
    return view('dashboard');
})->middleware(['auth', 'verified'])->name('dashboard');

Route::get('/admin', function () {
    return view('admin.index');
})->middleware(['auth', 'role:admin'])->name('admin.index');

Route::middleware('auth')->group(function () {
    Route::get('/profile', [ProfileController::class, 'edit'])->name('profile.edit');
    Route::patch('/profile', [ProfileController::class, 'update'])->name('profile.update');
    Route::delete('/profile', [ProfileController::class, 'destroy'])->name('profile.destroy');
});

